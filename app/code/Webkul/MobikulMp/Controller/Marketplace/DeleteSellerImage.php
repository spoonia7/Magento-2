namespace Webkul\MobikulMp\Controller\Marketplace;

/**
 * Class DeleteSellerImage
 *
 * @category Webkul
 * @package  Webkul_MobikulMp
 * @author   Webkul <support@webkul.com>
 * @license  https://store.webkul.com/license.html ASL Licence
 * @link     https://store.webkul.com/license.html
 */
class DeleteSellerImage extends AbstractMarketplace
{
    /**
     * Execute function for class DeleteSellerImage
     *
     * @throws LocalizedException
     *
     * @return json | void
     */
    public function execute()
    {
        try {
            $this->verifyRequest();
            $environment   = $this->emulate->startEnvironmentEmulation($this->storeId);
            $id            = 0;
            $collection    = $this->seller->getCollection()->addFieldToFilter("seller_id", $this->customerId);
            foreach ($collection as $value) {
                $id = $value->getId();
                if ($id != 0) {
                    $seller = $this->seller->load($id);
                    if ($this->entity == "banner") {
                        $seller->setBannerPic("");
                    }
                    if ($this->entity == "logo") {
                        $seller->setLogoPic("");
                    }
                    $seller->save();
                    $this->returnArray["message"] = __("Seller Image Deleted Successfully");
                    $this->returnArray["success"] = true;
                }
            }
            $this->emulate->stopEnvironmentEmulation($environment);
            $this->helper->log($this->returnArray, "logResponse", $this->wholeData);
            return $this->getJsonResponse($this->returnArray);
        } catch (\Exception $e) {
            $this->returnArray["message"] = __($e->getMessage());
            $this->helper->printLog($this->returnArray, 1);
            return $this->getJsonResponse($this->returnArray);
        }
    }

    /**
     * Verify Request function to verify Customer and Request
     *
     * @throws Exception customerNotExist
     * @return json | void
     */
    protected function verifyRequest()
    {
        if ($this->getRequest()->getMethod() == "POST" && $this->wholeData) {
            $this->storeId       = $this->wholeData["storeId"]     ?? 0;
            $this->entity        = $this->wholeData["entity"]    ?? 0;
            $this->customerToken = $this->wholeData["customerToken"] ?? '';
            $this->customerId    = $this->helper->getCustomerByToken($this->customerToken) ?? 0;
            if (!$this->customerId && $this->customerToken != "") {
                $this->returnArray["otherError"] = "customerNotExist";
                throw new \Magento\Framework\Exception\LocalizedException(
                    __("Customer you are requesting does not exist.")
                );
            } elseif ($this->customerId != 0) {
                $this->customerSession->setCustomerId($this->customerId);
            }
        } else {
            throw new \Exception(__("Invalid Request"));
        }
    }
}
